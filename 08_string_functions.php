<?php


$string = 'Hello World' ;

// String length
echo strlen($string);

// Position of 1st occurence of a substring in a string

echo strpos($string, 'o');

// Position of last occurence of a substring in a string

echo strpos($string, 'o');


// Reverse a String
echo strrev($string);

// Convert all chars to lowercase

echo strtolower($string);

// Convert all chars to uppercase

echo strtoupper($string);

// String replace

echo str_replace('World', 'Everyone', $string);
















